<template lang="html">
<div class="title">
        13_ImageTexture
    </div>
    <canvas id="canvas">
    </canvas>
    <pre  id="vertex-shader" type="x-shader/x-vertex">
        attribute vec4 a_position;
        attribute vec2 a_texcoord;
        
        uniform mat4 u_matrix;
        
        varying vec2 v_texcoord;
        
        void main() {
          gl_Position = u_matrix * a_position;
          v_texcoord = a_texcoord;
        }
    </pre>

    <pre  id="fragment-shader" type="x-shader/x-fragment">
        precision mediump float;
        varying vec2 v_texcoord;
        uniform sampler2D u_texture;
        
        void main() {
           gl_FragColor = texture2D(u_texture, v_texcoord);
        }
    </pre>
</template>
<script>
export default {
    name:'ImageTexture',
    mounted() {
        this.Render();
    },
    methods: {
        Render(){
                 function setGeometry(gl) {
            var positions = new Float32Array(
                [
                -0.5, -0.5,  -0.5,
                -0.5,  0.5,  -0.5,
                0.5, -0.5,  -0.5,
                -0.5,  0.5,  -0.5,
                0.5,  0.5,  -0.5,
                0.5, -0.5,  -0.5,

                -0.5, -0.5,   0.5,
                0.5, -0.5,   0.5,
                -0.5,  0.5,   0.5,
                -0.5,  0.5,   0.5,
                0.5, -0.5,   0.5,
                0.5,  0.5,   0.5,

                -0.5,   0.5, -0.5,
                -0.5,   0.5,  0.5,
                0.5,   0.5, -0.5,
                -0.5,   0.5,  0.5,
                0.5,   0.5,  0.5,
                0.5,   0.5, -0.5,

                -0.5,  -0.5, -0.5,
                0.5,  -0.5, -0.5,
                -0.5,  -0.5,  0.5,
                -0.5,  -0.5,  0.5,
                0.5,  -0.5, -0.5,
                0.5,  -0.5,  0.5,

                -0.5,  -0.5, -0.5,
                -0.5,  -0.5,  0.5,
                -0.5,   0.5, -0.5,
                -0.5,  -0.5,  0.5,
                -0.5,   0.5,  0.5,
                -0.5,   0.5, -0.5,

                0.5,  -0.5, -0.5,
                0.5,   0.5, -0.5,
                0.5,  -0.5,  0.5,
                0.5,  -0.5,  0.5,
                0.5,   0.5, -0.5,
                0.5,   0.5,  0.5,

                ]);
            gl.bufferData(gl.ARRAY_BUFFER, positions, gl.STATIC_DRAW);
        }


        function setTexcoords(gl) {
            gl.bufferData(
                gl.ARRAY_BUFFER,
                new Float32Array(
                    [
                    // select the top left image
                    0   , 0  ,
                    0   , 0.5,
                    0.25, 0  ,
                    0   , 0.5,
                    0.25, 0.5,
                    0.25, 0  ,
                    // select the top middle image
                    0.25, 0  ,
                    0.5 , 0  ,
                    0.25, 0.5,
                    0.25, 0.5,
                    0.5 , 0  ,
                    0.5 , 0.5,
                    // select to top right image
                    0.5 , 0  ,
                    0.5 , 0.5,
                    0.75, 0  ,
                    0.5 , 0.5,
                    0.75, 0.5,
                    0.75, 0  ,
                    // select the bottom left image
                    0   , 0.5,
                    0.25, 0.5,
                    0   , 1  ,
                    0   , 1  ,
                    0.25, 0.5,
                    0.25, 1  ,
                    // select the bottom middle image
                    0.25, 0.5,
                    0.25, 1  ,
                    0.5 , 0.5,
                    0.25, 1  ,
                    0.5 , 1  ,
                    0.5 , 0.5,
                    // select the bottom right image
                    0.5 , 0.5,
                    0.75, 0.5,
                    0.5 , 1  ,
                    0.5 , 1  ,
                    0.75, 0.5,
                    0.75, 1  ,

                ]),
                gl.STATIC_DRAW);
            }
        
        
        var fieldOfViewRadians = haruluya_webgl_utils.degToRad(60);
        var modelXRotationRadians = haruluya_webgl_utils.degToRad(0);
        var modelYRotationRadians = haruluya_webgl_utils.degToRad(0);

        var then = 0;

        var canvas = document.querySelector("#canvas");
        var gl = canvas.getContext("webgl");
        if (!gl) {
            return;
        }

        var program = haruluya_webgl_utils.createProgramFromScripts(gl, ["vertex-shader", "fragment-shader"]);
        var positionLocation = gl.getAttribLocation(program, "a_position");
        var texcoordLocation = gl.getAttribLocation(program, "a_texcoord");

        var matrixLocation = gl.getUniformLocation(program, "u_matrix");
        var textureLocation = gl.getUniformLocation(program, "u_texture");

        var positionBuffer = gl.createBuffer();
        gl.bindBuffer(gl.ARRAY_BUFFER, positionBuffer);
        setGeometry(gl);

        var texcoordBuffer = gl.createBuffer();
        gl.bindBuffer(gl.ARRAY_BUFFER, texcoordBuffer);

        setTexcoords(gl);

        var texture = gl.createTexture();
        gl.bindTexture(gl.TEXTURE_2D, texture);
        gl.texImage2D(gl.TEXTURE_2D, 0, gl.RGBA, 1, 1, 0, gl.RGBA, gl.UNSIGNED_BYTE,
                        new Uint8Array([0, 0, 255, 255]));


        var image = new Image();
        image.src = "noodles.jpg";
        image.addEventListener('load', function() {
            // Now that the image has loaded make copy it to the texture.
            gl.bindTexture(gl.TEXTURE_2D, texture);
            gl.texImage2D(gl.TEXTURE_2D, 0, gl.RGBA, gl.RGBA,gl.UNSIGNED_BYTE, image);

            // Check if the image is a power of 2 in both dimensions.
            if (isPowerOf2(image.width) && isPowerOf2(image.height)) {
            // Yes, it's a power of 2. Generate mips.
            gl.generateMipmap(gl.TEXTURE_2D);
            } else {
            // No, it's not a power of 2. Turn of mips and set wrapping to clamp to edge
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_S, gl.CLAMP_TO_EDGE);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_T, gl.CLAMP_TO_EDGE);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MIN_FILTER, gl.LINEAR);
            }
        });

        
        function isPowerOf2(value) {
            return (value & (value - 1)) === 0;
        }

        requestAnimationFrame(drawScene);

        function drawScene(time) {
            // convert to seconds
            time *= 0.001;
            // Subtract the previous time from the current time
            var deltaTime = time - then;
            // Remember the current time for the next frame.
            then = time;

            haruluya_webgl_utils.resizeCanvasToDisplaySize(gl.canvas);

            // Tell WebGL how to convert from clip space to pixels
            gl.viewport(0, 0, gl.canvas.width, gl.canvas.height);

            gl.enable(gl.CULL_FACE);
            gl.enable(gl.DEPTH_TEST);

            // Animate the rotation
            modelYRotationRadians += -0.7 * deltaTime;
            modelXRotationRadians += -0.4 * deltaTime;

            // Clear the canvas AND the depth buffer.
            gl.clear(gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);

            // Tell it to use our program (pair of shaders)
            gl.useProgram(program);

            // Turn on the position attribute
            gl.enableVertexAttribArray(positionLocation);

            // Bind the position buffer.
            gl.bindBuffer(gl.ARRAY_BUFFER, positionBuffer);

            // Tell the position attribute how to get data out of positionBuffer (ARRAY_BUFFER)
            var size = 3;          // 3 components per iteration
            var type = gl.FLOAT;   // the data is 32bit floats
            var normalize = false; // don't normalize the data
            var stride = 0;        // 0 = move forward size * sizeof(type) each iteration to get the next position
            var offset = 0;        // start at the beginning of the buffer
            gl.vertexAttribPointer(
                positionLocation, size, type, normalize, stride, offset);

            // Turn on the texcoord attribute
            gl.enableVertexAttribArray(texcoordLocation);

            // bind the texcoord buffer.
            gl.bindBuffer(gl.ARRAY_BUFFER, texcoordBuffer);

            // Tell the texcoord attribute how to get data out of texcoordBuffer (ARRAY_BUFFER)
            var size = 2;          // 2 components per iteration
            var type = gl.FLOAT;   // the data is 32bit floats
            var normalize = false; // don't normalize the data
            var stride = 0;        // 0 = move forward size * sizeof(type) each iteration to get the next position
            var offset = 0;        // start at the beginning of the buffer
            gl.vertexAttribPointer(
                texcoordLocation, size, type, normalize, stride, offset);

            // Compute the projection matrix
            var aspect = gl.canvas.clientWidth / gl.canvas.clientHeight;
            var projectionMatrix =
                haruluya_webgl_utils.perspective(fieldOfViewRadians, aspect, 1, 2000);

            var cameraPosition = [0, 0, 2];
            var up = [0, 1, 0];
            var target = [0, 0, 0];

            // Compute the camera's matrix using look at.
            var cameraMatrix = haruluya_webgl_utils.lookAt(cameraPosition, target, up);

            // Make a view matrix from the camera matrix.
            var viewMatrix = haruluya_webgl_utils.inverse(cameraMatrix);

            var viewProjectionMatrix = haruluya_webgl_utils.multiply3d(projectionMatrix, viewMatrix);

            var matrix = haruluya_webgl_utils.xRotate(viewProjectionMatrix, modelXRotationRadians);
            matrix = haruluya_webgl_utils.yRotate(matrix, modelYRotationRadians);

            // Set the matrix.
            gl.uniformMatrix4fv(matrixLocation, false, matrix);

            // Tell the shader to use texture unit 0 for u_texture
            gl.uniform1i(textureLocation, 0);

            // Draw the geometry.
            gl.drawArrays(gl.TRIANGLES, 0, 6 * 6);

            requestAnimationFrame(drawScene);
        }
        }
    },
}
</script>
